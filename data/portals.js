// Helper functions for portal management
function getPortalsInRegion(regionId) {
    return PORTALS.filter(portal => portal.region === regionId);
}

function getNearbyPortals(playerX, playerY, radius = 3) {
    return PORTALS.filter(portal => {
        const distance = Math.sqrt(Math.pow(portal.x - playerX, 2) + Math.pow(portal.y - playerY, 2));
        return distance <= radius;
    });
}

function getPortalById(id) {
    return PORTALS.find(portal => portal.id === id);
}